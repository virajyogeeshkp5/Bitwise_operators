# Bitwise operators
a=10
b=4
print("Bitwise operator")
# Binary AND
c = a&b
print("a&b:",c)
# Binary OR
c = a|b
print("a|b:",c)
# Binary XOR
c=a^b
print("a^b:",c)
# Binary ones Compliment
c=~a
print("~a:",c)
# Binary Left shift
c=a<<2
print("a<<2:",c)
# Binary Right shift
c=a>>2
print("a>>2:",c)